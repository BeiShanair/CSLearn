namespace LS10
{
    internal class Program
    {
        static void Main(string[] args)
        {
            #region 算数运算符

            #region 赋值符号
            // =
            int a = 1;
            Console.WriteLine(a);

            #endregion

            #region 算数运算符
            // 加 +
            int b = 1;
            int c = 2;
            int d = b + c;
            Console.WriteLine(d);

            // 减 -
            int e = 1;
            int f = 2;
            int g = e - f;
            Console.WriteLine(g);

            // 乘 *
            int h = 1;
            int i = 2;
            int j = h * i;
            Console.WriteLine(j);

            // 除 /
            int k = 1;
            int l = 2;
            int m = k / l;
            Console.WriteLine(m);

            #endregion


            #endregion
        }
    }
}
